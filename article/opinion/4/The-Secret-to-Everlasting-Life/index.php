      Not long ago I came to a sorry conclusion that all readers have or will come to at least once in their lives. It was this:
    </p>
    <p>I will never be able to read all the books I want to read.</p>
    <p>Sad, isn’t it? Worse, if we took on the age-old advice of ‘live each day as if it’s your last’, we’re again stuck, wondering just which book or books to attempt to consume that final, fatal day. A plethora of plays, poetry and novellas, in an attempt to read as much as possible? Perhaps a book by your favourite author: a Stephen King, a John Green? A Gillian Flynn or a Jodi Picoult? Or reread an old favourite? (Rereading. Was there ever a better way to get side-tracked?)</p>
    <p>A friend asked me recently whether I’d be happy to die midway through a book or series of books. And, on that note, whether there wasn’t a certain book or author’s work I’d want to die whilst in the middle of? Y’know, so people would know I had great taste after I’d died. After he asked me perhaps should’ve I began to wonder whether he may’ve been plotting to off me at some point later that afternoon, but I was all too consumed with a mild epiphanic (yes, I just discovered that word) horror. Which book would I want to die in the middle of? <i>Without</i> finishing?</p>
    <p><i>None</i>! As an avid reader and writer my whole life, the notion itself felt wrong. Blasphemous.</p>
    <p>At that time, I was a little over halfway through Harper Lee’s <i>To Kill A Mockingbird</i> and my mind conjured up the scene of my funeral. Members of my family stood around, shrouded in black, shaking their heads and muttering, ‘And she’ll never know what became of Scout and Atticus…’</p>
    <p>That night, despite having had long day, I stayed up late I finished 1960 classic…</p>
    <p>…and then almost immediately began Lee’s only other novel, <i>TKAM</i>’s sort-of sequel, <i>Go Set A Watchman</i>, letting myself get twenty pages in before finally turning my light off and calling it a night.</p>
    <p>I messaged my friend the next morning finally having made peace with the conundrum.</p>
    <p>‘So, I figured it out,’ I told him. ‘The book I could die in the middle of.’</p>
    <p>‘Are you still on that?’</p>
    <p>‘The answer,’ I ignored him, ‘is that I just wouldn’t.’</p>
    <p>‘Just wouldn’t what?’</p>
    <p>‘Die. I would simply refuse to die.’</p>
    <p>‘I think you’re avoiding the actual question.’</p>
    <p>‘You know how they say some people stay alive because they’re waiting for something? Permission to die or a loved to arrive in time to say goodbye? People sometimes live a little longer than expected because something important is unfinished. So, the plan is, when I’m older I’m just going to start reading really long series of books with unpredictable plots and engaging characters. I’ll read them and each time I start getting towards the end of a series, I’ll just start a new one at the same time, read that, and so on. Boom. Everlasting life. You’re welcome.’</p>
    <p>‘Hmm. You may actually be stubborn enough that that could work.’</p>
    <p>‘I’ll let you know.’</p>
    <p>‘Please do.’ There was a pause, and then, ‘But what if you die before your time? You get hit by a truck at 35 or something.’</p>
    <p>‘Well,’ I said, ‘if you get a truck driver’s license in the next ten years, I’ll just steer clear of you.’</p>
    <p>But in all seriousness, I still haven’t managed to properly answer his question. I just don’t know what book I’d be okay to die in the middle of. Something with a terrible ending I suppose, so I’d never be disappointed.</p>
    <p>In fact, the only fool-proof answer I’ve managed to come up with is to, right now, read the best book ever…and then never read anything again. I certainly wouldn’t die in the middle of a book that way. But what about music, dying in the middle of a song? Or a movie or a show or a trip? It’s a pretty deep hole you dig once you get started. The thing is, we’re all going to die in the middle of something, truck or no truck.</p>
    <p>I think the best thing you can do is just read. Read whatever you like, whatever you enjoy. Any fantasies of dying whilst clasping a beautiful gilt-edged copy <i>Jane Eyre</i> are just that: fantasies. (And carrying that around in your bag for the rest of your life is impractical and heavy.)</p>
    <p>If I die while reading then I know I’ll die happy, and so I’m going to read each day as if it’s my last. And enjoy it while I do.</p>
